import signal


class AppClass:
    def __init__(self, logger=None):
        self.logger = logger
        self.sigterm = False

    def is_shutdown(self):
        return self.sigterm

    def register_graceful_shutdown(self):
        signal.signal(signal.SIGINT, self.sigterm_handler)
        signal.signal(signal.SIGTERM, self.sigterm_handler)

    def sigterm_handler(self, signal, frame):
        self.sigterm = True
        print("Received SIGTERM, performing graceful shutdown ...")
        self.logger.warning("Received SIGTERM, performing graceful shutdown ...")

    def stdout_msg(self, msg=None, log=None, stdout=True) -> bool:
        if msg is None:
            return False
        if log is None and stdout is False:
            return False
        if log is not None:
            if log == "debug":
                self.logger.debug(msg)
            elif log == "info":
                self.logger.info(msg)
            elif log == "warn":
                self.logger.warn(msg)
            elif log == "error":
                self.logger.error(msg)
            elif log == "critical":
                self.logger.critical(msg)
            else:
                return False
        if stdout is True:
            print(msg)
        return True
